package com.project.my.dto;

public class FilmDto {
	
	private int movieIdx;
	private String title;
	private int movieGrade;
	private float averGrade;
	private int totalGrade;
	private int userCount;
	private String poster;
	private String director;
	private String actor;
	private String plot;
	private String runtime;
	private String genre;
	private String rating;
	private String releaseDate;
	
	public int getMovieIdx() {
		return movieIdx;
	}
	
	public void setMovieIdx(int movieIdx) {
		this.movieIdx = movieIdx;
	}
	
	public String getTitle() {
		return title;
	}
	
	public void setTitle(String title) {
		this.title = title;
	}
	
	public int getMovieGrade() {
		return movieGrade;
	}
	
	public void setMovieGrade(int movieGrade) {
		this.movieGrade = movieGrade;
	}
	
	public float getAverGrade() {
		return averGrade;
	}
	
	public void setAverGrade(float averGrade) {
		this.averGrade = averGrade;
	}
	
	public int getTotalGrade() {
		return totalGrade;
	}
	
	public void setTotalGrade(int totalGrade) {
		this.totalGrade = totalGrade;
	}
	
	public int getUserCount() {
		return userCount;
	}
	
	public void setUserCount(int userCount) {
		this.userCount = userCount;
	}
	
	public String getPoster() {
		return poster;
	}
	
	public void setPoster(String poster) {
		this.poster = poster;
	}
	
	public String getDirector() {
		return director;
	}
	
	public void setDirector(String director) {
		this.director = director;
	}
	
	public String getActor() {
		return actor;
	}
	
	public void setActor(String actor) {
		this.actor = actor;
	}
	
	public String getPlot() {
		return plot;
	}
	
	public void setPlot(String plot) {
		this.plot = plot;
	}
	
	public String getRuntime() {
		return runtime;
	}
	
	public void setRuntime(String runtime) {
		this.runtime = runtime;
	}
	
	public String getGenre() {
		return genre;
	}
	
	public void setGenre(String genre) {
		this.genre = genre;
	}
	
	public String getRating() {
		return rating;
	}
	
	public void setRating(String rating) {
		this.rating = rating;
	}
	
	public String getReleaseDate() {
		return releaseDate;
	}
	
	public void setReleaseDate(String releaseDate) {
		this.releaseDate = releaseDate;
	}
	
}
